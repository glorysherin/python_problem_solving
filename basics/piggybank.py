# write a program to calculate the total amount of money in the piggybank,given the coins of Rs 10,Rs 5,Rs 2,and Rs 1.
Ten= int(input("Enter the number of Ten rupees coins : "))
Five= int(input("Enter the number of Five rupees coins : "))
Two= int(input("Enter the number of Two rupees coins : "))
One= int(input("Enter the number of One rupees coins : "))
sum=(Ten*10 )+(Five*5)+(Two*2)+(One*1)
print("Total amount in the piggybank is :",sum)